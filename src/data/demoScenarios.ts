export function findDemoScenario(businessType: string, city: string): DemoScenario | null {
  const bt = businessType.toLowerCase();
  const ct = city.toLowerCase();

  return demoScenarios.find(s =>
    s.businessType.toLowerCase().includes(bt) || bt.includes(s.businessType.toLowerCase()) ||
    s.id.includes(bt)
  ) || demoScenarios.find(s =>
    ct.includes(s.city.toLowerCase())
  ) || null;
}

export function getDefaultDemoScenario(): DemoScenario {
  return demoScenarios[0];
}
